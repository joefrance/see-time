let image = new Jimp(5, 20, function (err, image) {
  if (err) throw err;

  imageData.forEach((row, y) => {
    row.forEach((color, x) => {
      image.setPixelColor(color, x, y);
    });
  });

  image.write('see-time.png', (err) => {
    if (err) throw err;
  });
});